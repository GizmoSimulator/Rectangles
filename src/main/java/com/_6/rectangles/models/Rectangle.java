package com._6.rectangles.models;

public class Rectangle {
    private String name;
    private double width;
    private double height;
    private String color;
    
    public Rectangle(String name, double width, double height, String color) {
        this.name = name;
        this.width = width;
        this.height = height;
        this.color = color;
    }

    
}
